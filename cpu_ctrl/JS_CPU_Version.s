GLOBAL		_JS_CPU_Version;int JS_CPU_Version(void)

I486FLAG	equ		0x00040000
NOTI486FLAG equ		0xfffbffff
_JS_CPU_Version:
	PUSHFD
	POP		EAX
	OR		EAX,I486FLAG
	PUSH	EAX
	POPFD
	
	PUSHFD
	POP		EAX
	AND		EAX,I486FLAG
	CMP		EAX,0X00
	JNE		i486
noti486:
	AND		EAX,NOTI486FLAG
	PUSH	EAX
	POPFD
	XOR		EAX,EAX
	RET
i486:
	AND		EAX,NOTI486FLAG
	PUSH	EAX
	POPFD
	MOV		EAX,0X00000001	
	RET